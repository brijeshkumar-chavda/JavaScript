// Boolean

// What
// - In JavaScript, the Boolean data type represents logical values, which can be either true or false.

// Why
// - The Boolean data type is used to represent conditions and make decisions in your program.
// - The Boolean data type provides a foundation for evaluating expressions and controlling the flow of your program.

// How
// - The Boolean data type in JavaScript supports various operations, such as logical operators (AND, OR, NOT) and comparison operators (equal, not equal, greater than, less than, etc.).
// - JavaScript automatically converts values to the Boolean data type when performing logical operations or comparisons.
// - The Boolean data type also includes special values, such as true and false, to represent logical truth and falsehood.

// Syntax
let boolean1 = true;
let boolean2 = false;