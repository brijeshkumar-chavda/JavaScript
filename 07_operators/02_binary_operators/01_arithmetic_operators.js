// Topic: Arithmetic Operators

let variable1 = 5;
let variable2 = 2;

// `+` (Addition)
console.log(variable1 + variable2);

// `-` (Subtraction)
console.log(variable1 - variable2);

// `*` (Multiplication)
console.log(variable1 * variable2);

// `/` (Division)
console.log(variable1 / variable2);

// `%` (Modulus)
console.log(variable1 % variable2);

// `**` (Exponentiation)
console.log(variable1 ** variable2);

// `++` (Increment)
console.log(variable1++);

// `--` (Decrement)
console.log(variable1--);
