// of()

// What
// - It will take multiple arguments and convert into array.

// Note
// - It will create array even if single argument is passed.
// - If no argument is passed, it will return empty array.
// - It will not flatten the array if array is passed as argument.

// Example
const array = Array.of(1, 2, 3, 4, 5, [6, 7, 8]);
console.log(array);
