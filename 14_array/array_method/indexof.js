// indexOf()

// What
// - It help to check the values index number.

// Note
// - It is case sensitive.
// - It return -1 if the value is not found in the array.
// - It return the first index number if there are multiple same values in the array.

// Example
const array = [1, 2, 3, 4, 5];
console.log(array.indexOf(5));
