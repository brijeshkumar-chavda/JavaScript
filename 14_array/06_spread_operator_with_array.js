// Topic: Spread operator With Array

// Q. What is Spread Operator?
// -> This operator is used to expand or spread iterable elements like arrays or strings into individual elements.

// Example:
const array1 = [1, 2, 3, 4, 5];
const array2 = [6, 7, 8, 9, 10];

const newArray = [...array1, ...array2];
console.log(newArray);
