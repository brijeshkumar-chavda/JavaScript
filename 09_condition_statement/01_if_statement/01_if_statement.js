// Topic: If statement

// Q. What is If statement?
// -> The if statement is a conditional statement in JavaScript that allows you to execute a block of code only if a specified condition evaluates to true. It is used to control the flow of execution based on certain conditions.

// Syntax:
// if (condition) {
//   // Code to be executed if the condition is true
// }

// Example:
const variable = true;
if (variable) {
  console.log("Condition is true");
}
  