// Topic: Independent If statement

// Q. What is Independent If statement?
// -> An independent if statement refers to multiple if statements that are not connected to each other. Each if statement evaluates its own condition independently, and all of them can be executed if their respective conditions are true.

// Syntax:
const variable = true;

if (variable) {
  console.log("Condition is true");
}

if (!variable) {
  console.log("Condition is false");
}
