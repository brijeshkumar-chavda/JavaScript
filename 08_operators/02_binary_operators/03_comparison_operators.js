// Topic: Comparison Operators

// `==` (Equal to)
let variable1 = 1;
let variable2 = "1";
console.log(variable1 == variable2); // Output: true

// `!=` (Not equal to)
console.log(variable1 != variable2); // Output: false

// `===` (Strict equal to)
console.log(variable1 === variable2); // Output: false

// `!==` (Strict not equal to)
console.log(variable1 !== variable2); // Output: true

// `>` (Greater than)
console.log(variable1 > variable2); // Output: false

// `<` (Less than)
console.log(variable1 < variable2); // Output: false

// `>=` (Greater than or equal to)
console.log(variable1 >= variable2); // Output: false

// `<=` (Less than or equal to)
console.log(variable1 <= variable2); // Output: false
