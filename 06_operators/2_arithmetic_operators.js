// Arithmetic Operators

// - `+` (Addition)
// - `-` (Subtraction)
// - `*` (Multiplication)
// - `/` (Division)
// - `%` (Modulus)
// - `**` (Exponentiation)
// - `++` (Increment)
// - `--` (Decrement)

let myVariable1 = 5;
let myVariable2 = 2;
console.log(myVariable1 + myVariable2);
console.log(myVariable1 - myVariable2);
console.log(myVariable1 * myVariable2);
console.log(myVariable1 / myVariable2);
console.log(myVariable1 % myVariable2);
console.log(myVariable1 ** myVariable2);
console.log(myVariable1++);
console.log(myVariable1--);
