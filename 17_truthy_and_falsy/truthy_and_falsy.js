// Truthy VS Falsy

// Falsy: Any value which is treated as a false. Known as a falsy value.
// false (Boolean literal)
// 0 (Zero)
// -0 (Negative zero)
// 0n (BigInt zero)
// "" (Empty string)
// null
// undefined
// NaN (Not-a-Number)

// Truthy: Any value other than falsy.
// true (Boolean literal)
// " " (Any non-empty string)
// 123 (Any non-zero number)
// []
// {}
// function(){}
