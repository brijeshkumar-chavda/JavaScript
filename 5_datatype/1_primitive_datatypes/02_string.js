// String

// What
// - In JavaScript, the String data type represents textual data, enclosed in quotes.

// Why
// - The String data type is used to work with textual data in JavaScript.
// - The String data type provides a foundation for manipulating and displaying text in your program.

// How
// - The String data type in JavaScript supports various operations, such as concatenation, comparison, and extraction of substrings.
// - JavaScript automatically converts numeric values to the String data type when performing operations that require textual data.
// - The String data type also includes special characters, such as escape sequences and template literals, to represent and manipulate text.

